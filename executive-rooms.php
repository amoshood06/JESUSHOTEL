<script>
  let currentExecIndex = 0;
  const slider = document.getElementById('execSlider');
  const dots = document.querySelectorAll('.exec-dot');

  function updateExecSlider() {
    slider.style.transform = `translateX(-${currentExecIndex * 100}%)`;
    dots.forEach((dot, i) => {
      dot.style.backgroundColor = i === currentExecIndex ? '#D48255' : '#D1D5DB';
    });
  }

  function moveExecSlide(direction) {
    const totalSlides = slider.children.length;
    currentExecIndex = (currentExecIndex + direction + totalSlides) % totalSlides;
    updateExecSlider();
  }

  function jumpExecSlide(index) {
    currentExecIndex = index;
    updateExecSlider();
  }
</script>


